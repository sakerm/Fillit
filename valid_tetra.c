int		ft_tetra(char **tetra)
{
	int x;
	int y;
	int ht;

	ht = 0;
	x = 0;
	y = 0;
	while (y <= 3 && x <= 3)
	{
		if (tetra[y][x] == '#' && tetra[y][x + 1] != '#' &&
			tetra[y][x] == '#' && tetra[y][x - 1] != '#' &&
			tetra[y][x] == '#' && tetra[y + 1][x] != '#' &&
			tetra[y][x] == '#' && tetra[y - 1][x] != '#')
			return (0);
		if (x == 0)
		{
			x = 0;
			y++;
		}
		else
			x++;
	}
	return (1);
}
